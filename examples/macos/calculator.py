"""Run some automations to test things"""
from __future__ import unicode_literals
from __future__ import print_function


"""
Example script for Calculator on macOS 10.14
"""

import sys
sys.path.append(".")

from pywinauto import Application
from pywinauto.timings import Timings

app = Application().start('Calculator')

dlg = app.Window.Calculator
Timings.slow()
dlg.type_keys('2*3=')
Timings.slow()

clear_button = app.Window.clear
clear_button.click()

dlg.type_keys('5+7=')
Timings.slow()

app.close()
