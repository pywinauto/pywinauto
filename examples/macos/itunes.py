"""
Example script for iTunes on macOS 10.14
"""

import time, sys
from pywinauto import Application

app = Application().start('Itunes')

dlg = app.Window
#dlg.dump_tree()

library = dlg.Library
library.click()

music_button = dlg.Music
music_button.click()

#dlg.dump_tree()

store = dlg.child_window(name="Go to the iTunes Store")
store.click()

time.sleep(5)
dlg.Search.type_keys('michael jackson')

dlg.Radio.click()
dlg.Music.click()

app.close()
